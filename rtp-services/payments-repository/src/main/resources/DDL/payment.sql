create table PAYMENT(ID BIGINT NOT NULL AUTO_INCREMENT, 
DOMESTIC_PAYMENT_ID VARCHAR(256) NULL, 
CONSENT_ID VARCHAR(256) NULL, 
STATUS VARCHAR(60000) NULL, 
CREATION_DATE_TIME TIMESTAMP NULL, 
STATUS_UPDATE_DATE_TIME TIMESTAMP NULL,
REFUND_ACCOUNT_SCHEME_NAME VARCHAR(256) NULL, 
REFUND_ACCOUNT_IDENTIFICATION VARCHAR(256) NULL, 
REFUND_ACCOUNT_NAME VARCHAR(256) NULL, 
INITIATION_INSTRUCTION_IDENTIICATION VARCHAR(256) NULL, 
INITIATION_END_TO_END_IDENTIFICATION VARCHAR(256) NULL, 
INITIATION_INSTRUCTED_AMOUNT VARCHAR(256) NULL, 
INITIATION_INSTRUCTED_CURRENCY VARCHAR(256) NULL,
DEBTOR_ACCOUNT_SCHEME_NAME VARCHAR(256) NULL, 
DEBTOR_ACCOUNT_IDENTIFICATION VARCHAR(256) NULL, 
DEBTOR_ACCOUNT_NAME VARCHAR(256) NULL, 
DEBTOR_ACCOUNT_SECONDARY_IDENTIFICATION VARCHAR(256) NULL, 
CREDITOR_ACCOUNT_SCHEME_NAME VARCHAR(256) NULL, 
CREDITOR_ACCOUNT_IDENTIFICATION VARCHAR(256) NULL, 
CREDITOR_ACCOUNT_NAME VARCHAR(256) NULL, 
CREDITOR_ACCOUNT_SECONDARY_IDENTIFICATION VARCHAR(256) NULL, 
REMITTANCE_INFORMATION_REFERENCE VARCHAR(256) NULL, 
REMITTANCE_INFORMATION_UNSTRUCTURED VARCHAR(256) NULL, PRIMARY KEY (ID));